/*
[1][1] = 1 [1][2] = 2 [1][3] = 3 [1][4] = 4 [1][5] = 5  [1][6] = 6
[2][1] = 2 [2][2] = 2 [2][3] = 4 [2][4] = 4 [2][5] = 6  [2][6] = 6
[3][1] = 3 [3][2] = 4 [3][3] = 3 [3][4] = 6 [3][5] = 7  [3][6] = 6
[4][1] = 4 [4][2] = 4 [4][3] = 6 [4][4] = 4 [4][5] = 8  [4][6] = 8
[5][1] = 5 [5][2] = 6 [5][3] = 7 [5][4] = 8 [5][5] = 5  [5][6] = 10
[6][1] = 6 [6][2] = 6 [6][3] = 6 [6][4] = 4 [6][5] = 10 [6][6] = 6
*/

function solution(w, h) {
    const _gcd = gcd(w, h);
    
    return (w * h) - (w / _gcd + h / _gcd - 1) * _gcd;
}

function gcd (a, b) {
    return !b ? a : gcd(b, a % b);
}

